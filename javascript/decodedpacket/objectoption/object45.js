// Door
DecodedPacketObjectOption.prototype.object_11726 = function(player, index, object) {
    if (object.isBusy()) {
        return;
    }
    if ((object.getDirection() == 0 || object.getDirection() == 2) && player.getY() != object.getY()) {
        return;
    } else if ((object.getDirection() == 1 || object.getDirection() == 3) && player.getX() != object.getX()) {
        return;
    }
    if (index == 1) {
        player.getGameEncoder().sendMessage("You attempt to pick the lock.");
    }
    if (index == 1 && !player.getInventory().hasItem(1523)) {
        player.getGameEncoder().sendMessage("You need a lockpick for this lock.");
        return;
    }
    if (player.getX() != object.getX() || player.getY() != object.getY()) {
        if (index == 1) {
            player.getGameEncoder().sendMessage("The door is already unlocked.");
            return;
        }
    } else {
        if (index == 0) {
            player.getGameEncoder().sendMessage("The door is locked.");
            return;
        } else if (Utils.randomE(4) != 0) {
            player.getGameEncoder().sendMessage("You fail to pick the lock.");
            return;
        }
    }
    if (index == 0) {
        player.getGameEncoder().sendMessage("You go through the door.");
    } else if (index == 1) {
        player.getGameEncoder().sendMessage("You manage to pick the lock.");
    }
    player.getMovement().clear();
    if (object.getDirection() == 0) {
        if (player.getX() == object.getX()) {
            player.getMovement().addMovement(object.getX() - 1, object.getY());
        } else {
            player.getMovement().addMovement(object.getX(), object.getY());
        }
    } else if (object.getDirection() == 1) {
        if (player.getY() == object.getY()) {
            player.getMovement().addMovement(object.getX(), object.getY() + 1);
        } else {
            player.getMovement().addMovement(object.getX(), object.getY());
        }
    } else if (object.getDirection() == 2) {
        if (player.getX() == object.getX()) {
            player.getMovement().addMovement(object.getX() + 1, object.getY());
        } else {
            player.getMovement().addMovement(object.getX(), object.getY());
        }
    } else if (object.getDirection() == 3) {
        if (player.getY() == object.getY()) {
            player.getMovement().addMovement(object.getX(), object.getY() - 1);
        } else {
            player.getMovement().addMovement(object.getX(), object.getY());
        }
    }
    Region.openDoor(player, object, 1, false, false);
}

